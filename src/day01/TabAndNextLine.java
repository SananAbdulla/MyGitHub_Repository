package day01;

public class TabAndNextLine {
    public static void main(String[] args) {

        System.out.println("A Sentence"); // has no space
        System.out.println(" A Sentence"); // has one spaces
        System.out.println("  A Sentence"); // has two spaces
        System.out.println("   A Sentence"); // has three spaces
        System.out.println("    A Sentence"); // has four spaces



        System.out.println("\tA Sentence"); // has four spaces
        System.out.println("\t\tA Sentence"); // has 8 spaces


        System.out.println("1) Unlock the card door\n2) Get into the car\n3) Start the car");

    }
}
